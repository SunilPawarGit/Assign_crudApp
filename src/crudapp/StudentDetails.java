    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        // TODO add your handling code here:
         try
        {
           if(tb_search.getText().length() != 0){
               
                DefaultTableModel defmodel = (DefaultTableModel)dgv_all.getModel();
                defmodel.setRowCount(0);
                connect();
                String sql = "SELECT * from tbl_Student where Id='"+tb_search.getText()+"'";
                st = con.createStatement();
                rs = st.executeQuery(sql);
            
                while(rs.next()){

                    String id = String.valueOf(rs.getInt("Id"));
                    String name = rs.getString("Name");
                    String dob = rs.getString("DOB");
                    String doj = rs.getString("DOJ");

                    String tbldata[] = {id,name,dob,doj};
                    defmodel.addRow(tbldata);

                }
                con.close();
           }
           else
           {
               JOptionPane.showMessageDialog(this,"Please Enter Id");
               tb_search.requestFocus();
           }
            
            con.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btn_searchActionPerformed

