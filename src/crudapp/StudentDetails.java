    private void dgv_allMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dgv_allMouseClicked
        // TODO add your handling code here:
        try {
        int row = dgv_all.rowAtPoint(evt.getPoint());
        if(JOptionPane.showConfirmDialog(this,"Are You Update it?","Update",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE) == JOptionPane.YES_OPTION ){
            String id = dgv_all.getValueAt(row, 0).toString();
            String name = dgv_all.getValueAt(row, 1).toString();
            String dob = dgv_all.getValueAt(row, 2).toString();
            String doj = dgv_all.getValueAt(row, 3).toString();
            
            tb_id.setText(id);
            tb_name.setText(name);
            Date db =new SimpleDateFormat("dd-mm-yyyy").parse(dob);
            Date dj =new SimpleDateFormat("dd-mm-yyyy").parse(doj);
            
            dt_dob.setDate(db);
            dt_join.setDate(dj);
            
                btn_update.setEnabled(true);
                btn_add.setEnabled(false);
            } 
        }
        catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        
        
    }//GEN-LAST:event_dgv_allMouseClicked

