    public void viewdata(){
         try
        {
            DefaultTableModel defmodel = (DefaultTableModel)dgv_all.getModel();
            defmodel.setRowCount(0);
            connect();
            String sql = "SELECT * from tbl_Student";
            st = con.createStatement();
            rs = st.executeQuery(sql);
            
            while(rs.next()){
                
                String id = String.valueOf(rs.getInt("Id"));
                String name = rs.getString("Name");
                String dob = rs.getString("DOB");
                String doj = rs.getString("DOJ");
                
                String tbldata[] = {id,name,dob,doj};
                
                defmodel.addRow(tbldata);
                
            }
            
            con.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        
        try
        {
            if(tb_id.getText().length() != 0 && tb_name.getText().length() != 0 && dt_dob.getDate() != null && dt_join.getDate() != null){
                connect();
            Date d1 = dt_dob.getDate();
            Date d2 = dt_join.getDate();
            SimpleDateFormat df = new SimpleDateFormat("dd-MM-yyyy");
            String doj = df.format(d2);
            String dob = df.format(d1);
            String sql = "INSERT into tbl_Student (Id,Name,DOB,DOJ) values ('"+tb_id.getText()+"','"+tb_name.getText()+"','"+dob+"','"+doj+"')";
            st = con.createStatement();
            st.execute(sql);
           
            JOptionPane.showMessageDialog(this,"Data Inserted Successfully");
            
            autoclr();
            con.close();
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btn_addActionPerformed
    
