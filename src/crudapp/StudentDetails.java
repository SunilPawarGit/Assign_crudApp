    public void autoclr(){
        try{
            tb_id.setText("");
            tb_name.setText("");
            dt_join.setDate(null);
            dt_dob.setDate(null);
            
            viewdata();
            
            btn_update.setEnabled(false);
            btn_add.setEnabled(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
